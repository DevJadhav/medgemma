def main():
    parser = argparse.ArgumentParser(
        description="Generate secure secrets for MedAI Compass"
    )
    parser.add_argument(
        "--output", "-o",
        type=str,
        default=".env",
        help="Output file path (default: .env)"
    )
    parser.add_argument(
        "--force", "-f",
        action="store_true",
        help="Overwrite existing secrets"
    )
    parser.add_argument(
        "--example-only",
        action="store_true",
        help="Only create .env.example file"
    )
    
    args = parser.parse_args()
    
    # Determine project root
    script_dir = Path(__file__).parent
    project_root = script_dir.parent
    
    print("\n" + "=" * 60)
    print("  MedAI Compass - Secret Generator")
    print("=" * 60 + "\n")
    
    # Always create/update .env.example
    create_env_example(project_root)
    
    if args.example_only:
        print("\n  Done (example only mode)")
        return
    
    # Generate secrets
    print("\nGenerating secrets...")
    new_secrets = generate_all_secrets()
    
    # Load existing .env if present
    env_path = project_root / args.output
    existing_env = load_existing_env(env_path)
    
    # Add default values for non-secret variables
    defaults = {
        "POSTGRES_HOST": "postgres",
        "POSTGRES_PORT": "5432",
        "POSTGRES_USER": "medai",
        "POSTGRES_DB": "medai_compass",
        "REDIS_HOST": "redis",
        "REDIS_PORT": "6379",
        "API_HOST": "0.0.0.0",
        "API_PORT": "8000",
        "CORS_ORIGINS": "*",
        "ENVIRONMENT": "development",
        "LOG_LEVEL": "INFO",
        "DEBUG": "false",
        "MINIO_ENDPOINT": "minio:9000",
        "MINIO_ACCESS_KEY": "minioadmin",
        "MINIO_SECRET_KEY": "minioadmin123",
        "GRAFANA_PASSWORD": "admin",
    }
    
    for key, value in defaults.items():
        if key not in existing_env:
            existing_env[key] = value
    
    # Handle force flag
    if args.force:
        print("  (Force mode: overwriting all secrets)")
        existing_env.update(new_secrets)
        write_env_file(env_path, existing_env, {})
    else:
        write_env_file(env_path, existing_env, new_secrets)
    
    # Security reminder
    print("\n" + "-" * 60)
    print("  IMPORTANT SECURITY NOTES:")
    print("-" * 60)
    print("  1. Add .env to .gitignore (if not already)")
    print("  2. Never commit secrets to version control")
    print("  3. Use different secrets for each environment")
    print("  4. Back up production secrets securely")
    print("-" * 60 + "\n")
    
    # Check for HuggingFace token
    if not existing_env.get("HF_TOKEN") and not existing_env.get("HUGGING_FACE_HUB_TOKEN"):
        print("  ⚠ WARNING: HF_TOKEN not set!")
        print("    MedGemma models require a HuggingFace token.")
        print("    Get one at: https://huggingface.co/settings/tokens")
        print()
    
    # Check for Modal tokens
    if not existing_env.get("MODAL_TOKEN_ID"):
        print("  ℹ INFO: Modal tokens not set")
        print("    For cloud GPU access, run: modal token new")
        print()


if __name__ == "__main__":
    main()
