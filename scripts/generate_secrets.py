#!/usr/bin/env python3
"""Generate secure secrets for MedAI Compass deployment.

This script generates cryptographically secure secrets for:
- POSTGRES_PASSWORD
- REDIS_PASSWORD
- JWT_SECRET
- PHI_ENCRYPTION_KEY (Fernet-compatible)

Usage:
    python scripts/generate_secrets.py [--output .env] [--force]
"""

import argparse
import secrets
import base64
import os
from pathlib import Path
from cryptography.fernet import Fernet


def generate_password(length: int = 32) -> str:
    """Generate a secure random password."""
    return secrets.token_urlsafe(length)


def generate_jwt_secret(length: int = 64) -> str:
    """Generate a secure JWT secret."""
    return secrets.token_hex(length)


def generate_fernet_key() -> str:
    """Generate a Fernet encryption key for PHI."""
    return Fernet.generate_key().decode()


def generate_all_secrets() -> dict:
    """Generate all required secrets."""
    return {
        "POSTGRES_PASSWORD": generate_password(24),
        "REDIS_PASSWORD": generate_password(24),
        "JWT_SECRET": generate_jwt_secret(32),
        "PHI_ENCRYPTION_KEY": generate_fernet_key(),
    }


def load_existing_env(env_path: Path) -> dict:
    """Load existing .env file if it exists."""
    env_vars = {}
    if env_path.exists():
        with open(env_path, "r") as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    key, _, value = line.partition("=")
                    # Remove quotes if present
                    value = value.strip().strip('"').strip("'")
                    env_vars[key.strip()] = value
    return env_vars


def write_env_file(env_path: Path, env_vars: dict, secrets: dict) -> None:
    """Write secrets to .env file, preserving existing variables."""
    # Merge: existing vars + new secrets (secrets don't overwrite if already set)
    for key, value in secrets.items():
        if key not in env_vars or not env_vars[key]:
            env_vars[key] = value
            print(f"  ✓ Generated: {key}")
        else:
            print(f"  → Kept existing: {key}")
    
    # Write the .env file
    with open(env_path, "w") as f:
        f.write("# MedAI Compass Environment Configuration\n")
        f.write("# Auto-generated secrets - DO NOT COMMIT TO VERSION CONTROL\n")
        f.write("#\n")
        f.write("# Generated by: python scripts/generate_secrets.py\n")
        f.write("#\n\n")
        
        # Group variables by category
        categories = {
            "Database": ["POSTGRES_HOST", "POSTGRES_PORT", "POSTGRES_USER", "POSTGRES_PASSWORD", "POSTGRES_DB"],
            "Redis": ["REDIS_HOST", "REDIS_PORT", "REDIS_PASSWORD"],
            "Security": ["JWT_SECRET", "PHI_ENCRYPTION_KEY"],
            "API": ["API_HOST", "API_PORT", "CORS_ORIGINS", "ENVIRONMENT", "LOG_LEVEL", "DEBUG"],
            "HuggingFace": ["HF_TOKEN", "HUGGING_FACE_HUB_TOKEN"],
            "Modal (Cloud GPU)": ["MODAL_TOKEN_ID", "MODAL_TOKEN_SECRET"],
            "Storage": ["MINIO_ACCESS_KEY", "MINIO_SECRET_KEY", "MINIO_ENDPOINT"],
            "Monitoring": ["GRAFANA_PASSWORD"],
            "Datasets": ["PHYSIONET_USERNAME", "PHYSIONET_PASSWORD"],
            "Other": [],
        }
        
        written_keys = set()
        
        for category, keys in categories.items():
            category_vars = {k: env_vars[k] for k in keys if k in env_vars}
            if category_vars:
                f.write(f"# {category}\n")
                for key, value in category_vars.items():
                    f.write(f'{key}="{value}"\n')
                    written_keys.add(key)
                f.write("\n")
        
        # Write any remaining variables
        other_vars = {k: v for k, v in env_vars.items() if k not in written_keys}
        if other_vars:
            f.write("# Other\n")
            for key, value in other_vars.items():
                f.write(f'{key}="{value}"\n')
    
    print(f"\n  ✓ Written to: {env_path}")


